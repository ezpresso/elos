#include <kern/system.h>
#include <kern/symbol.h>
#include <lib/string.h>

void strreverse(char *str) {
	char tmp, *src, *dst;
	size_t len;

	len = strlen(str);
	if(len < 1) {
		return;
	}

	src = str;
	dst = src + len - 1;
	while(src < dst) {
	  	tmp = *src;
	 	*src++ = *dst;
		*dst-- = tmp;
	}
}

export(strreverse);